function hinduDayName(day, poonam) {
  let paksha = 0;
  if (poonam) day -= 15;
  day = modPos(day, 30);
  if (day == 0) return lang3Data.dayNames[0];
  if (day == 15) return lang3Data.dayNames[15];
  if (day >= 16) {
    day -= 15;
    paksha = 1;
  }
  let dayName = "";
  dayName += lang3Data.dayTypes[paksha] + " ";
  dayName += lang3Data.dayNames[day];
  return dayName;
}
